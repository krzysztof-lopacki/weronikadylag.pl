            <p>
              Six years ago, I returned to painting working with acrylics. In my recent art, I focus on psychology and
              positive emotions by dissecting colour, form and composition. My inspiration comes from the works of Yago
              Hortal, Nicolaos Schizas, Orlanda Bloom, Lauren Mycroft, Jan Kalab as well as from my professional graphic
              design experience.
            </p>
            <p>
              To emphasize the elusiveness of emotional states and reality itself, I developed a technique that allows
              me to work with fluid paint and flows of air with only a few brushstrokes. This technique allows me to
              keep a thin layer of paint and create, with almost no brushstrokes, paintings in a print-like style. There
              is a freeze of motion noticeable in the paintings and a paper-like paint folding created with a rapid
              change of air direction.
            </p>
          </div>
          <div class="col-sm-6 py-4">
            <p>
              I work on white, blue-ish, black, red and raw linen backgrounds – usually one colour to make the
              interaction of the colours stand out.
            </p>
            <p>
              My interest in psychology and the need to represent positive emotions in paintings were born out of a deep
              conviction that mental health maintenance relies on a steady focus on positive aspects of life. Our
              realities grow stale and difficult at times. I intend my work to be a stop where viewers can recharge and
              energize to move forward against the adversities of life.
            </p>
