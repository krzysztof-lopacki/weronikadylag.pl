                        <p>So how do you fold a paint? And can you even fold something that is a liquid and is laying on a flat
                            surface?</p>
                        <p>At many of my paintings I am using the force to push the paint across the canvases and to push it out.
                            And on all of my paintings you are able to see this movement. But while working on “Look inside” painting,
                            I discovered that I can also fold it. What do I mean? Well the paint is rapidly changing direction
                            creating a new layer of paint without destroying the layer beneath, creating this sort of paper folding
                            effect.</p>
                        <p>This also a way to have control over the paint and being able to create central composition that is
