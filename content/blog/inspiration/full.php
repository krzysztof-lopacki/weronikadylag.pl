                        <p>Working with fluid paint is entering process with a vague plan in mind and working while reacting to what
                            is happening on the canvases. But that is just working with fluid. I have decided to work with air as
                            well. I liked the idea and the effect that air is having on the paint. The air is beautifully showing the
                            movement of the paint but also creates the ripples that match with the subject of the paintings as well.
                            The fluid comes in waves just like the emotions. And just like the emotions are unpredictable the effects
                            of work with fluid paint are too.</p>
